 * @throws VerificationFailed if authentication fails — opaque.
 * @throws UnsupportedOperationException if [supportsSyncChaChaPoly] is `false` (e.g. web).
 */
internal expect fun chaChaPolyOpen(
    key: ChaChaPolyKey,

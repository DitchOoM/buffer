package com.ditchoom.buffer.crypto

import com.ditchoom.buffer.BufferFactory
import com.ditchoom.buffer.Default
import com.ditchoom.buffer.PlatformBuffer
import com.ditchoom.buffer.ReadBuffer
import com.ditchoom.buffer.WriteBuffer

/*
 * Authenticated Encryption with Associated Data (AEAD).
 *
 * This file owns the AEAD primitive family: AES-GCM (128/256-bit keys) and ChaCha20-Poly1305
 * (IETF). Each algorithm is wrapped over the platform's native stack — JCA (AES/GCM/NoPadding,
 * ChaCha20-Poly1305) on JVM/Android, CommonCrypto/CryptoKit on Apple, and WebCrypto (AES-GCM)
 * on js/wasmJs. We never hand-roll a primitive; an unavailable algorithm throws
 * UnsupportedOperationException and its capability flag is false.
 *
 * Security contract (uniform across platforms):
 *  - Plaintext is released only after the tag verifies. open() performs a native one-shot
 *    decrypt; there is no unverified-plaintext streaming. A bad tag, tampered ciphertext,
 *    swapped AAD, or wrong key all surface as the same opaque VerificationFailed with no
 *    distinguishing reason — a granular reason on a verify path is a decryption oracle.
 *  - Nonces are CSPRNG-generated by default. The seal helpers draw a fresh 12-byte nonce from
 *    the library CSPRNG and prepend it to the output, so a caller cannot accidentally reuse
 *    one. The explicit-nonce path is internal (the KAT/Wycheproof seam) — reusing a
 *    (key, nonce) pair with GCM or ChaCha20-Poly1305 is catastrophic (see the threat model).
 *  - Fixed 96-bit nonce, fixed 128-bit tag. Any other nonce length is rejected with
 *    IllegalArgumentException; truncated tags are never produced or accepted.
 *  - Typed, sized keys. AesGcmKey carries its 128/256-bit size and ChaChaPolyKey is a distinct
 *    type, so a key for one algorithm/size cannot be silently used for another.
 *
 * Output framing — the self-framing one-shot helpers emit: nonce(12) ‖ ciphertext ‖ tag(16),
 * so the sealed buffer is self-describing and open needs no separate nonce argument. The
 * lower-level seal/open expect functions take ciphertext+tag positioned by the caller within
 * dest; the framing helpers build on them.
 */

/** AEAD nonce/IV length in bytes (96-bit, the only length these wrappers accept). */
const val AEAD_NONCE_BYTES: Int = 12

/** AEAD authentication-tag length in bytes (128-bit, fixed). */
const val AEAD_TAG_BYTES: Int = 16

/** Key length in bytes for AES-128-GCM. */
const val AES_128_KEY_BYTES: Int = 16

/** Key length in bytes for AES-256-GCM. */
const val AES_256_KEY_BYTES: Int = 32

/** Key length in bytes for ChaCha20-Poly1305 (always 256-bit). */
const val CHACHA_KEY_BYTES: Int = 32

private const val SYNC_AES_GCM_UNSUPPORTED = "synchronous AES-GCM is not supported on this platform"

/**
 * An AES-GCM key, carrying its [size] so a 128-bit key can never be silently used where a
 * 256-bit key was meant (or vice versa). Construct via [AesGcmKey.of]; the key bytes are
 * copied into a fresh [PlatformBuffer] so the caller's source buffer is decoupled from the
 * key's lifetime.
 *
 * The key material is **not** auto-wiped — callers that need erase-on-free should allocate
 * the backing buffer from `BufferFactory.deterministic().secure()` and pass it through
 * [of]; the copy taken here preserves that secure backing when the supplied [factory] is
 * secure.
 */
class AesGcmKey private constructor(
    /** 128 or 256 — the AES key size in bits. */
    val sizeBits: Int,
    internal val material: PlatformBuffer,
) : AutoCloseable {
    private var closed = false

    /** Key size in bytes (16 for AES-128, 32 for AES-256). */
    val sizeBytes: Int get() = sizeBits / 8

    /**
     * Zeroes and frees the key material (wipes when [material] is a secure buffer). Idempotent.
     * Parity with the signing/key-agreement key types, which are already [AutoCloseable].
     */
    override fun close() {
        if (!closed) {
            closed = true
            material.freeNativeMemory()
        }
    }

    companion object {
        /**
         * Wraps [key]'s remaining bytes as an AES-GCM key. The length must be exactly 16
         * (AES-128) or 32 (AES-256) bytes; any other length is rejected. The bytes are
         * copied into a buffer from [factory] (use a secure factory for erase-on-free).
         */
        fun of(
            key: ReadBuffer,
            factory: BufferFactory = BufferFactory.Default,
        ): AesGcmKey {
            val n = key.remaining()
            require(n == AES_128_KEY_BYTES || n == AES_256_KEY_BYTES) {
                "AES-GCM key must be $AES_128_KEY_BYTES or $AES_256_KEY_BYTES bytes, was $n"
            }
            return AesGcmKey(n * Byte.SIZE_BITS, copyMaterial(key, factory))
        }
    }
}

/**
 * A ChaCha20-Poly1305 (IETF) key — always 256-bit. A distinct type from [AesGcmKey] so the
 * two AEAD families cannot be cross-used. See [AesGcmKey] for the secure-backing note.
 */
class ChaChaPolyKey private constructor(
    internal val material: PlatformBuffer,
) : AutoCloseable {
    private var closed = false

    /**
     * Zeroes and frees the key material (wipes when [material] is a secure buffer). Idempotent.
     * Parity with the signing/key-agreement key types, which are already [AutoCloseable].
     */
    override fun close() {
        if (!closed) {
            closed = true
            material.freeNativeMemory()
        }
    }

    companion object {
        /**
         * Wraps [key]'s remaining bytes as a ChaCha20-Poly1305 key. The length must be
         * exactly 32 bytes; any other length is rejected. The bytes are copied into a buffer
         * from [factory] (use a secure factory for erase-on-free).
         */
        fun of(
            key: ReadBuffer,
            factory: BufferFactory = BufferFactory.Default,
        ): ChaChaPolyKey {
            val n = key.remaining()
            require(n == CHACHA_KEY_BYTES) {
                "ChaCha20-Poly1305 key must be $CHACHA_KEY_BYTES bytes, was $n"
            }
            return ChaChaPolyKey(copyMaterial(key, factory))
        }
    }
}

/** Copies [source]'s remaining bytes into a fresh read-ready buffer (non-destructive). */
private fun copyMaterial(
    source: ReadBuffer,
    factory: BufferFactory,
): PlatformBuffer = copyBuffer(source, factory)


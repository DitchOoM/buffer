// =============================================================================
// Internal framing helpers (shared by sync + native async)
// =============================================================================

/** Allocates a `nonce ‖ ciphertext ‖ tag` buffer sized for [plaintextLen] bytes of plaintext. */
internal fun allocateFramed(
    plaintextLen: Int,
    factory: BufferFactory,
): PlatformBuffer = factory.allocate(AEAD_NONCE_BYTES + plaintextLen + AEAD_TAG_BYTES)

/**
 * Writes a fresh CSPRNG nonce at [dest]'s current position (advancing past it) and returns a
 * read-ready [AEAD_NONCE_BYTES]-byte view of just-written nonce for use as the seal nonce.
 */
internal fun writeFreshNonce(dest: WriteBuffer): ReadBuffer {
    val nonce = cryptoRandom(AEAD_NONCE_BYTES)
    // Bulk-copy the nonce into dest's framing slot, then hand back the standalone nonce buffer.
    // copyInto is non-destructive, so the returned nonce stays read-ready at position 0.
    copyInto(nonce, dest)
    return nonce
}

/** Decomposes a `nonce ‖ ciphertext ‖ tag` buffer into (nonce, ciphertext‖tag, plaintextLen). */
internal fun splitFramed(sealed: ReadBuffer): FramedParts {
    val total = sealed.remaining()
    require(total >= AEAD_NONCE_BYTES + AEAD_TAG_BYTES) {
        "sealed buffer too short: need at least ${AEAD_NONCE_BYTES + AEAD_TAG_BYTES} bytes, has $total"
    }
    val start = sealed.position()
    val nonce = sliceView(sealed, start, AEAD_NONCE_BYTES)
    val ctAndTagLen = total - AEAD_NONCE_BYTES
    val ctAndTag = sliceView(sealed, start + AEAD_NONCE_BYTES, ctAndTagLen)
    val plaintextLen = ctAndTagLen - AEAD_TAG_BYTES
    return FramedParts(nonce, ctAndTag, plaintextLen)
}

/** A non-destructive read-ready view of [length] bytes of [source] starting at absolute [from]. */
private fun sliceView(
    source: ReadBuffer,
    from: Int,
    length: Int,
): ReadBuffer {
    val savedPos = source.position()
    val savedLimit = source.limit()
    source.position(from)
    source.setLimit(from + length)
    val view = source.slice()
    source.position(savedPos)
    source.setLimit(savedLimit)
    return view
}

internal class FramedParts(
    val nonce: ReadBuffer,
    val ciphertextAndTag: ReadBuffer,
    val plaintextLen: Int,
) {
    operator fun component1() = nonce

    operator fun component2() = ciphertextAndTag

    operator fun component3() = plaintextLen
}

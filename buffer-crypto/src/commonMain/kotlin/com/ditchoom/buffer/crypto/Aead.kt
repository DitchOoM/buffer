// =============================================================================
// Self-framing one-shot helpers (synchronous) — nonce ‖ ciphertext ‖ tag
// =============================================================================

/**
 * Seals [plaintext] with AES-GCM under [key], generating a fresh CSPRNG nonce and returning a
 * read-ready `nonce ‖ ciphertext ‖ tag` buffer (allocated via [factory]). The matching
 * [aesGcmOpen] framing helper recovers the plaintext with no separate nonce argument.
 *
 * This is the recommended sealing entry point: the nonce is never caller-supplied, so a
 * (key, nonce) pair cannot be accidentally reused.
 *
 * @throws UnsupportedOperationException if [supportsSyncAesGcm] is `false` (use [aesGcmSealAsync]).
 */
fun aesGcmSeal(
    key: AesGcmKey,
    plaintext: ReadBuffer,
    aad: ReadBuffer? = null,
    factory: BufferFactory = BufferFactory.Default,
): PlatformBuffer {
    if (!supportsSyncAesGcm) throw UnsupportedOperationException(SYNC_AES_GCM_UNSUPPORTED)
    val out = allocateFramed(plaintext.remaining(), factory)
    val nonce = writeFreshNonce(out)
    aesGcmSeal(key, nonce, aad, plaintext, out)
    out.resetForRead()
    return out
}

/**
 * Opens a `nonce ‖ ciphertext ‖ tag` buffer produced by [aesGcmSeal], returning the recovered
 * plaintext (allocated via [factory]). Plaintext is returned only if the tag verifies.
 *
 * @throws VerificationFailed if authentication fails — opaque.
 * @throws UnsupportedOperationException if [supportsSyncAesGcm] is `false` (use [aesGcmOpenAsync]).
 */
fun aesGcmOpen(
    sealed: ReadBuffer,
    key: AesGcmKey,
    aad: ReadBuffer? = null,
    factory: BufferFactory = BufferFactory.Default,
): PlatformBuffer {
    if (!supportsSyncAesGcm) throw UnsupportedOperationException(SYNC_AES_GCM_UNSUPPORTED)
    val (nonce, ctAndTag, ptLen) = splitFramed(sealed)
    val out = factory.allocate(ptLen)
    aesGcmOpen(key, nonce, aad, ctAndTag, out)
    out.resetForRead()
    return out
}

/**
 * ChaCha20-Poly1305 self-framing seal — see [aesGcmSeal]. Generates a fresh CSPRNG nonce.
 *
 * @throws UnsupportedOperationException if [supportsSyncChaChaPoly] is `false` (e.g. web — never polyfilled).
 */
fun chaChaPolySeal(
    key: ChaChaPolyKey,
    plaintext: ReadBuffer,
    aad: ReadBuffer? = null,
    factory: BufferFactory = BufferFactory.Default,
): PlatformBuffer {
    if (!supportsSyncChaChaPoly) {
        throw UnsupportedOperationException("ChaCha20-Poly1305 is not supported on this platform")
    }
    val out = allocateFramed(plaintext.remaining(), factory)
    val nonce = writeFreshNonce(out)
    chaChaPolySeal(key, nonce, aad, plaintext, out)
    out.resetForRead()
    return out
}

/**
 * ChaCha20-Poly1305 self-framing open — see [aesGcmOpen].
 *
 * @throws VerificationFailed if authentication fails — opaque.
 * @throws UnsupportedOperationException if [supportsSyncChaChaPoly] is `false` (e.g. web).
 */
fun chaChaPolyOpen(
    sealed: ReadBuffer,
    key: ChaChaPolyKey,
    aad: ReadBuffer? = null,

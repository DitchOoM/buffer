/**
 * Whether this platform supports the synchronous AES-GCM [aesGcmSeal] / [aesGcmOpen].
 *
 * - JVM, Android, Apple: `true` (JCA / CryptoKit).
 * - js/wasmJs: `false` — WebCrypto's `SubtleCrypto` is async-only; use [aesGcmSealAsync] /
 *   [aesGcmOpenAsync] instead.
 */
expect val supportsSyncAesGcm: Boolean

/**
 * Whether this platform supports ChaCha20-Poly1305 at all.
 *
 * - JVM (11+), Android, Apple: `true`.
 * - js/wasmJs: `false` — ChaCha20-Poly1305 is **not** part of WebCrypto and we never
 *   polyfill it. Both the sync and async ChaCha entry points throw
 *   [UnsupportedOperationException] on the web.
 */
expect val supportsChaChaPoly: Boolean

/**
 * Whether this platform supports the synchronous ChaCha20-Poly1305
 * [chaChaPolySeal] / [chaChaPolyOpen]. Equal to [supportsChaChaPoly] today (no async ChaCha
 * path exists on any current target).
 */
expect val supportsSyncChaChaPoly: Boolean

/**
 * Seals [plaintext] with AES-GCM under [key] and [nonce], writing `ciphertext ‖ tag` into
 * [dest] at its current position (ciphertext length == plaintext length, then [AEAD_TAG_BYTES]
 * of tag). [aad] (associated data, authenticated-not-encrypted) may be `null`/empty.
 *
 * [nonce] must be exactly [AEAD_NONCE_BYTES] bytes. [dest] must have
 * `plaintext.remaining() + AEAD_TAG_BYTES` bytes remaining. The caller is responsible for
 * nonce uniqueness — prefer the framing helper [aesGcmSeal] (with `dest`+`factory`) which
 * generates the nonce.
 *
 * @throws UnsupportedOperationException if [supportsSyncAesGcm] is `false`.
 */
internal expect fun aesGcmSeal(
    key: AesGcmKey,
    nonce: ReadBuffer,
    aad: ReadBuffer?,
    plaintext: ReadBuffer,
    dest: WriteBuffer,
)

/**
 * Opens an AES-GCM `ciphertext ‖ tag` blob ([ciphertextAndTag], whose last [AEAD_TAG_BYTES]
 * bytes are the tag) under [key], [nonce], and [aad], writing the recovered plaintext into
 * [dest]. The plaintext is written **only if the tag verifies**.
 *
 * @throws VerificationFailed if authentication fails (bad tag/ciphertext/AAD/key) — opaque.
 * @throws UnsupportedOperationException if [supportsSyncAesGcm] is `false`.
 */
internal expect fun aesGcmOpen(
    key: AesGcmKey,
    nonce: ReadBuffer,
    aad: ReadBuffer?,
    ciphertextAndTag: ReadBuffer,
    dest: WriteBuffer,
)

/**
 * ChaCha20-Poly1305 counterpart of [aesGcmSeal]. See it for the layout/precondition contract.
 *
 * @throws UnsupportedOperationException if [supportsSyncChaChaPoly] is `false` (e.g. web).
 */
internal expect fun chaChaPolySeal(
    key: ChaChaPolyKey,
    nonce: ReadBuffer,
    aad: ReadBuffer?,
    plaintext: ReadBuffer,
    dest: WriteBuffer,
)

/**
 * ChaCha20-Poly1305 counterpart of [aesGcmOpen]. See it for the verify contract.
